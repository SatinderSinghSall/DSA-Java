//! if-else: Java

package assignments;

public class ifelse {
    public static void main(String[] args) {
        int a = 46;
        int b = 76;

        if (a > b) {
            System.out.println("A is bigger than B");
        } else {
            System.out.println("B is bigger than A");
        }

        System.out.println("End of the code!");
    }
}
